package com.sri.ai.util.base;

import java.util.List;

import com.google.common.annotations.Beta;
import com.google.common.base.Function;

/**
 * A function for converting a list of two elements to a pair.
 * @author braz
 */
@Beta
public class ListToPair<T, T1, T2> implements Function<List<T>, Pair<T1,T2>> {

	@SuppressWarnings("unchecked")
	@Override
	public Pair<T1,T2> apply(List<T> list) {
		return Pair.make((T1) list.get(0), (T2) list.get(1));
	}
}
